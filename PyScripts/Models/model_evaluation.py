import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.model_selection import cross_val_score, cross_validate
from sklearn.model_selection import TimeSeriesSplit

def classification_accuracy(predictions, actuals) -> tuple[float, float]:
    if (type(predictions)!='numpy.array'):
        predictions=(pd.Series(predictions)>0).astype(int).to_numpy()
    avg_pred_direction=np.mean(predictions)
    accuracy=np.mean(predictions==actuals)
    return accuracy, avg_pred_direction

def display_feat_importances_tree(model, X: pd.DataFrame, n: int =50) -> None:
    model_feature_df=pd.DataFrame({
        'Feature': X.columns,
        'Importance': model.feature_importances_
    }).sort_values(by='Importance', ascending=False)
    model_feature_df.head(n=n).plot(kind='barh', x="Feature", y="Importance")
    plt.xlabel("Feature Importance")
    plt.xticks(rotation=45)
    plt.ylabel("Feature Name")
    plt.show()

def display_coef_importances_regression(model, X: pd.DataFrame, n: int =50) -> None:
    importances=np.abs(model.coef_[0]) 
    model_feature_df=pd.DataFrame({
        'Feature': X.columns,
        'Importance': importances
    }).sort_values(by='Importance', ascending=False)
    model_feature_df.head(n=n).plot(kind='barh', x="Feature", y="Importance")
    plt.gca().invert_yaxis()
    plt.show()


def get_final_metrics_grid(grid_obj, X_test, y_test):
    idx=grid_obj.best_index_
    results=grid_obj.cv_results_
   
    mean_train=results['mean_train_score'][idx]
    std_train=results['std_train_score'][idx]
   
    mean_cv_test=results['mean_test_score'][idx]
    std_cv_test=results['std_test_score'][idx]
   
    final_score=grid_obj.score(X_test, y_test)
   
    print(f"--- Model Report ---")
    print(f"Avg Train Accuracy:    {mean_train:.4f} (±{std_train:.4f})")
    print(f"Avg CV Test Accuracy: {mean_cv_test:.4f} (±{std_cv_test:.4f})") # This test score is generated by splitting the training set into time series splits.
    print(f"Final Hold-out (Test) Score (Accuracy): {final_score:.4f}")
   
    return mean_cv_test, std_cv_test

def rolling_window_backtest(model, X, y, window_size=21, horizon=5, verbose=0):
    results=[]

    n=len(X)
    total_iterations = (n - horizon - window_size) // horizon + 1
    if (verbose != 0): print(f"Rolling Window Backtest over {total_iterations} iterations.")
    current_step=0
    for i in range(window_size, n - horizon, horizon):
        current_step += 1
        X_train_roll=X.iloc[i-window_size : i]
        y_train_roll=y.iloc[i-window_size : i]
        
        X_test_roll=X.iloc[i : i+horizon]
        y_test_roll=y.iloc[i : i+horizon]
        
        model.fit(X_train_roll, y_train_roll)
        preds=model.predict(X_test_roll)
        
        acc=classification_accuracy(y_test_roll, preds)
        results.append(acc)
        if (verbose>0 and ((i%10) == 0)): print(f"{current_step * 100 / total_iterations:.2f}% complete. Current iteration: {current_step}, True iteration: {i + 1 - window_size}")
        
    print(f"Average Rolling Accuracy (Test): {np.mean(results):.4f} (±{np.std(results):.4f})")
    return results


def classification_wfv_eval(model, X_train: pd.DataFrame, y_train: pd.DataFrame, n: int =24, max_train_size: int =21, test_size: int =5):
    tscv=TimeSeriesSplit(n_splits=n, max_train_size=max_train_size, test_size=test_size)
    scoring_metrics=['accuracy', 'precision', 'recall']
    cv_scores=cross_val_score(model, X_train, y_train, cv=tscv, n_jobs=-1)
    results=cross_validate(model, X_train, y_train, cv=tscv, scoring=scoring_metrics)

    print(f"Average Accuracy:   {results['test_accuracy'].mean():.2%}")
    print(f"Standard Deviation: {cv_scores.std() * 100:.2f}%")
    print(f"Average Precision:  {results['test_precision'].mean():.2%}")
    print(f"Average Recall:     {results['test_recall'].mean():.2%}")

class ModelResults:
    """Class to store and compare classification model results"""
    def __init__(self):
        self.results_df = pd.DataFrame(columns=['Model', 'Accuracy', 'Precision', 'Recall', 'F1-Score'])
    
    def add_result(self, model_name: str, accuracy: float, precision: float, recall: float, f1_score: float):
        """Add model results to the comparison dataframe"""
        new_result = pd.DataFrame({
            'Model': [model_name],
            'Accuracy': [accuracy],
            'Precision': [precision],
            'Recall': [recall],
            'F1-Score': [f1_score]
        })
        self.results_df = pd.concat([self.results_df, new_result], ignore_index=True)
    
    def display_results(self):
        """Display all model results in a formatted table"""
        print("\n" + "="*80)
        print("MODEL COMPARISON RESULTS")
        print("="*80)
        print(self.results_df.to_string(index=False))
        print("="*80 + "\n")
    
    def save_results(self, filepath: str):
        """Save results to CSV file"""
        self.results_df.to_csv(filepath, index=False)
        print(f"Results saved to {filepath}")
    
    def get_best_model(self, metric: str = 'F1-Score'):
        """Get the best model based on specified metric"""
        if metric in self.results_df.columns:
            best_idx = self.results_df[metric].idxmax()
            return self.results_df.loc[best_idx]
        else:
            print(f"Metric '{metric}' not found in results")
